void main(){
    int x, y;

    // Bitwise Boolean Logical Operators = Bitwise Logical Operators

    x = 5; y = 4;
    System.out.println(x++ > 10 & y++ < 10);
    System.out.println(x);      // 6
    System.out.println(y);      // 5

    // Boolean Conditional Operators = Conditional Operators = Conditional Logical Operators

    System.out.println("-----------------");
    x = 5; y = 4;
    System.out.println(x++ > 10 && y++ < 10);
    System.out.println(x);      // 6
    System.out.println(y);      // 4
}